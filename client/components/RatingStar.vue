<template>
  <fieldset :class="[`starability-${kind}`, 'rating']">
    <legend v-if="legend">{{ legend }}</legend>
    <template v-for="(item, index) in items">
      <input type="radio" :id="uuid(index)" name="rating" :value="item.value" :checked="hasChecked(index + 1)" v-on:change="change(index + 1)">
      <label class="material-icons" :for="uuid(index)" :title="item.title || ''">{{(selected > index) ? "star" : "star_border"}}</label>
    </template>
  </fieldset>
</template>

<script>
export default {
  name: 'rating-star',
  props: {
    uid: {
      type: String,
    },
    legend: String,
    items: {
      type: Array,
      default: () => ([])
    },
    value: {
      type: Number,
      default: -1
    },
    kind: {
      type: String,
      default: 'basic',
      validator: (val) => ['basic', 'slot', 'grow', 'growRotate', 'fade', 'checkmark'].indexOf(val) > -1
    }
  },
  data () {
    return {
      selected: this.value
    }
  },

  methods: {
    uuid (index) {
      // generated uid might be different from server and from client
      return `rating-${this.uid}-item-${index}`
    },
    hasChecked (index) {
      return this.selected === index
    },
    change (index) {
      this.selected = index
      this.$emit('change', this.selected)
    }
  },
  computed: {
    count () {
      return this.items.length
    }
  }
}
</script>
